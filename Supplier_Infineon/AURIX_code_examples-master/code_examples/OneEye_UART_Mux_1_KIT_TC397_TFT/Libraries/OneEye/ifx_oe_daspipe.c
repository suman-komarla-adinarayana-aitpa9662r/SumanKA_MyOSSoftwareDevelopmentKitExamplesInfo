#include "ifx_oe_daspipe.h"
#include "string.h"
#define IFX_OE_DASPIPE_MARKER "1eyePipe"

boolean Ifx_Oe_DasPipe_Write(Ifx_Oe_DasPipe* dPipe, void* data, Ifx_Oe_SizeT* count, Ifx_Oe_TickTime timeout)
{
    Ifx_Oe_SizeT left;
    boolean      result = TRUE;

    if (*count != 0)
    {
        left              = Ifx_Oe_DasFifo_write(&dPipe->fifo, data, *count, timeout);
        dPipe->writeCount = dPipe->writeCount + (*count - left);
        *count           -= left;
        result            = left == 0;
    }

    return result;
}


boolean Ifx_Oe_DasPipe_Read(Ifx_Oe_DasPipe* dPipe, void* data, Ifx_Oe_SizeT* count, Ifx_Oe_TickTime timeout)
{
    Ifx_Oe_SizeT left = Ifx_Oe_DasFifo_read(&dPipe->fifo, data, count, timeout);
    dPipe->readCount = dPipe->readCount + *count;
    return left == 0;
}


void Ifx_Oe_DasPipe_ClearRx(Ifx_Oe_DasPipe* dPipe)
{
    Ifx_Oe_DasFifo_clearRx(&dPipe->fifo);
}


void Ifx_Oe_DasPipe_ClearTx(Ifx_Oe_DasPipe* dPipe)
{
    Ifx_Oe_DasFifo_clearTx(&dPipe->fifo);
}


void Ifx_Oe_DasPipe_initConfig(Ifx_Oe_DasPipe_Config* config)
{
    config->size = 512;
}


boolean Ifx_Oe_DasPipe_init(Ifx_Oe_DasPipe* dpipe, Ifx_Oe_DasPipe_Config* config)
{
    Ifx_Oe_DasFifo_Config fifoCfg;
    Ifx_Oe_DasFifo_initConfig(&fifoCfg);
    fifoCfg.size = config->size;

    if (Ifx_Oe_DasFifo_init(&dpipe->fifo, &fifoCfg) == FALSE)
    {
        return FALSE;
    }

    /* must be done only after complete initialization */
    strncpy((char*)&dpipe->oneEyeDebugPipe.marker, IFX_OE_DASPIPE_MARKER, sizeof(dpipe->oneEyeDebugPipe.marker));

    return TRUE;
}


boolean Ifx_Oe_DasPipe_stdIfDPipeInit(IfxStdIf_DPipe* stdif, Ifx_Oe_DasPipe* dpipe)
{
    /* Ensure the stdif is reset to zeros */
    memset(stdif, 0, sizeof(IfxStdIf_DPipe));

    /* Set the API link */
    stdif->driver  = (IfxStdIf_InterfaceDriver)dpipe;
    stdif->write   = (IfxStdIf_DPipe_Write) & Ifx_Oe_DasPipe_Write;
    stdif->read    = (IfxStdIf_DPipe_Read) & Ifx_Oe_DasPipe_Read;
    stdif->clearTx = (IfxStdIf_DPipe_ClearTx) & Ifx_Oe_DasPipe_ClearTx;
    stdif->clearRx = (IfxStdIf_DPipe_ClearRx) & Ifx_Oe_DasPipe_ClearRx;
    return TRUE;
}
