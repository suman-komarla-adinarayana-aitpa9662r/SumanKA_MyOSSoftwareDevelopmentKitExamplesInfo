#include "CCU_Clock.h"
#include "IfxScuCcu.h"
#include "IfxScu_PinMap.h"
#include "IfxPort.h"

/* Note: For backward compatibility, some of the registers and functions have the prefix "SCU" (System Control Unit).
 * In AURIX™ TC2xx, the clocking system was a part of the System Control Unit.
 */

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define PLL_FREQ        200000000.0f                                         /* PLL frequency in Hz                 */
#define SPB_FREQ        PLL_FREQ / 2                                         /* SPB frequency in Hz                 */
#define PINOUT          &MODULE_P23, 1                                       /* Port 23 pin 1 for CCU clock output  */
#define FDR_STEP        0x3CE                                                /* Divider for out frequency           */
#define DIV_MODE        0x1                                                  /* Divider mode: 0x1 is normal mode    */
#define EXT_CLK         0x0                                                  /* Set external clock: 0x00 is Fout    */
#define EXT_CLK_EN      0x1                                                  /* Enable external clock               */
#define CLK_SEL         0x1                                                  /* Select clock source: 0x1            */

/*********************************************************************************************************************/
/*--------------------------------------------Function Implementations-----------------------------------------------*/
/*********************************************************************************************************************/
/* This function configures the output for the CCU clock */
void configOutputCcuClock(void)
{
    /* Remove safety to allow register modification */
    IfxScuWdt_clearSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());

    SCU_CCUCON0.B.CLKSEL = CLK_SEL;                                          /* Select the clock source             */
    SCU_EXTCON.B.EN0     = EXT_CLK_EN;                                       /* Enable external clock               */
    SCU_EXTCON.B.SEL0    = EXT_CLK;                                          /* Select Fout as external clock       */
    SCU_FDR.B.STEP       = FDR_STEP;                                         /* Set FDR step                        */
    SCU_FDR.B.DM         = DIV_MODE;                                         /* Set divider mode to normal          */

    /* Set safety to allow register modification */
    IfxScuWdt_setSafetyEndinitInline(IfxScuWdt_getSafetyWatchdogPasswordInline());

    /* Select CCU external clock as output (port pin P23.1) and set the pad driver to high speed */
    IfxPort_setPinMode(PINOUT, IfxPort_Mode_outputPushPullAlt6);
    IfxPort_setPinPadDriver(PINOUT, IfxPort_PadDriver_cmosAutomotiveSpeed1);
}

/* This function initializes the CCU clock */
void initCcuClock(void)
{
    IfxScuCcu_Config ccuClockConfig;                        /* Define the clock configuration structure             */
    IfxScuCcu_initConfig(&ccuClockConfig);                  /* Initialize the configuration for with default values */

    /* Set the PLL dividers */
    ccuClockConfig.pllInitialStepConfig.pllsParameters.sysPllConfig.nDivider = IfxScuCcu_Ndivider_20;
    ccuClockConfig.pllInitialStepConfig.pllsParameters.sysPllConfig.k2Divider = IfxScuCcu_K2divider_2;
    ccuClockConfig.pllInitialStepConfig.pllsParameters.sysPllConfig.pDivider = IfxScuCcu_Pdivider_1;

    /* Initialize the CCU */
    IfxScuCcu_init(&ccuClockConfig);

    /* Set the Serial Peripheral Bus (SPB) frequency */
    IfxScuCcu_setSpbFrequency(SPB_FREQ);

    /* Configure the CCU clock output */
    configOutputCcuClock();
}
