#include "ifx_oneeyedaspipe.h"
#include "string.h"
#define IFX_ONEEYEDASPIPE_MARKER "1eyePipe"

boolean Ifx_OneEyeDasPipe_Write (Ifx_OneEyeDasPipe * dPipe, void *data, Ifx_SizeT *count, Ifx_TickTime timeout)
{

    Ifx_SizeT left;
    boolean result = TRUE;
    if (*count != 0)
    {
        left = Ifx_OneEyeDasFifo_write(&dPipe->fifo, data, *count, timeout);
        dPipe->writeCount = dPipe->writeCount  + (*count - left);
        *count -= left;
        result = left == 0;
    }

    return result;

}

boolean Ifx_OneEyeDasPipe_Read (Ifx_OneEyeDasPipe * dPipe, void *data, Ifx_SizeT *count, Ifx_TickTime timeout)
{
    Ifx_SizeT left = Ifx_OneEyeDasFifo_read(&dPipe->fifo, data, count, timeout);
    dPipe->readCount = dPipe->readCount  + *count;
    return left == 0;
}

void Ifx_OneEyeDasPipe_ClearRx (Ifx_OneEyeDasPipe * dPipe)
{
    Ifx_OneEyeDasFifo_clearRx(&dPipe->fifo);
}

void Ifx_OneEyeDasPipe_ClearTx (Ifx_OneEyeDasPipe * dPipe)
{
    Ifx_OneEyeDasFifo_clearTx(&dPipe->fifo);
}

void Ifx_OneEyeDasPipe_initConfig(Ifx_OneEyeDasPipe_Config * config)
{
	config->size = 512;
}


boolean Ifx_OneEyeDasPipe_init (Ifx_OneEyeDasPipe * dpipe, Ifx_OneEyeDasPipe_Config * config)
{
    Ifx_OneEyeDasFifo_Config fifoCfg;
    Ifx_OneEyeDasFifo_initConfig(&fifoCfg);
    fifoCfg.size = config->size;

    if(Ifx_OneEyeDasFifo_init(&dpipe->fifo, &fifoCfg) == FALSE){
        return FALSE;
    }

    /* must be done only after complete initialization */
    strncpy((char *)&dpipe->oneEyeDebugPipe.marker, IFX_ONEEYEDASPIPE_MARKER, sizeof(dpipe->oneEyeDebugPipe.marker));

    return TRUE;
}


boolean Ifx_OneEyeDasPipe_stdIfDPipeInit (IfxStdIf_DPipe *stdif, Ifx_OneEyeDasPipe *dpipe)
{
    /* Ensure the stdif is reset to zeros */
    memset(stdif, 0, sizeof(IfxStdIf_DPipe));

    /* Set the API link */
    stdif->driver = (IfxStdIf_InterfaceDriver) dpipe;
    stdif->write = (IfxStdIf_DPipe_Write) &Ifx_OneEyeDasPipe_Write;
    stdif->read = (IfxStdIf_DPipe_Read) &Ifx_OneEyeDasPipe_Read;
    stdif->clearTx = (IfxStdIf_DPipe_ClearTx) &Ifx_OneEyeDasPipe_ClearTx;
    stdif->clearRx = (IfxStdIf_DPipe_ClearRx) &Ifx_OneEyeDasPipe_ClearRx;
    return TRUE;
}
