/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define LED1                &MODULE_P13,0        /* LED D107: Port 13, Pin 0 */
#define LED2                &MODULE_P13,1        /* LED D108: Port 13, Pin 1 */

#define DUMMY_VALUE_1       0xA
#define DUMMY_VALUE_2       0xB

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Initialize the used peripherals */
void initPeripherals(void)
{
    /* Set the port pin 13.0 (to which the LED 107 is connected) to output push-pull mode */
    IfxPort_setPinModeOutput(LED1, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);
    /* Set the port pin 13.1 (to which the LED 108 is connected) to output push-pull mode */
    IfxPort_setPinModeOutput(LED2, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);

    /* Turn off LEDs (LEDs are low-level active) */
    IfxPort_setPinHigh(LED1);
    IfxPort_setPinHigh(LED2);

    /* Enable ASCLIN module in order to be able to write on its registers */
    IfxAsclin_enableModule(&MODULE_ASCLIN0);

    /* Wait until ASCLIN is enabled */
    while(IfxAsclin_getModuleStatus(&MODULE_ASCLIN0) != 0);
}

/* Test the registers protection access functionality */
void testRegistersAccessProtection(void)
{
    /* Write to Data Configuration Register of ASCLIN0 while the access protection is NOT enabled */
    MODULE_ASCLIN0.DATCON.B.DATLEN = DUMMY_VALUE_1;

    /* Check whether the configuration is written or not */
    if(MODULE_ASCLIN0.DATCON.B.DATLEN == DUMMY_VALUE_1)
    {
        /* Switch ON LED1 */
        IfxPort_setPinLow(LED1);
    }

    /* Protect ASCLIN0 Registers from CPU0 Writes */
    /* Get Safety Watchdog Password */
    uint16 safetyPassword = IfxScuWdt_getSafetyWatchdogPassword();

    /* Clear Safety EndInit protection */
    IfxScuWdt_clearSafetyEndinit(safetyPassword);

    /* Disable CPU0 access to ASCLIN0 registers */
    MODULE_ASCLIN0.ACCEN0.B.EN1 = 0x0;

    /* Restore Safety EndInit protection */
    IfxScuWdt_setSafetyEndinit(safetyPassword);

    /* Write once again to Data Configuration Register of ASCLIN0.
     * This time the CPU0 is not allowed to access ASCLIN0 registers, thus a bus error Trap is expected.
     */
    MODULE_ASCLIN0.DATCON.B.DATLEN = DUMMY_VALUE_2;
}

/* Hook for the Bus Error Trap Service Routine */
void busErrorTSRHook(IfxCpu_Trap trapWatch)
{
    /* A bus error trap is generated, check whether the CPU0 was able to write to ASCLIN0 register */
    if(MODULE_ASCLIN0.DATCON.B.DATLEN != DUMMY_VALUE_2)
    {
        /* The configuration is not written, Switch ON LED2 */
        IfxPort_setPinLow(LED2);
    }
}

