/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
/* DMA related Macros */

/* DMA Channel used in this example (0 lowest prio -- 127 highest prio) */
/* When DMA is used as Service Provider, the channel's priority should be equal to the UART related IR priority */
#define DMA_CHANNEL             INTPRIO_ASCLIN0_RX

/* Start address of the DLMU0 - AURIX TC39xx */
#define DEST_ADDR               0x90000000

/* ASCLIN UART related Macros */
#define SERIAL_BAUDRATE         115200                      /* Set the baud rate                                    */
#define UART_PIN_RX             IfxAsclin0_RXB_P15_3_IN     /* UART receive port pin                                */
#define UART_PIN_TX             IfxAsclin0_TX_P15_3_OUT     /* UART transmit port pin                               */
#define UART_RX_BUFFER_SIZE     64                          /* Definition of the RX buffer size                     */
#define UART_TX_BUFFER_SIZE     64                          /* Definition of the TX buffer size                     */

/* Definition of the interrupt priorities */
#define INTPRIO_ASCLIN0_TX      11                          /* Triggered when AscLin transmits                      */
#define INTPRIO_ASCLIN0_RX      12                          /* Triggered when AscLin receives                       */
#define INTPRIO_DMA             13                          /* Triggered when a DMA transaction is finished         */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
uint32 g_correctTransactions = 0;                       /* Incremented when a correct DMA transaction is finished   */

IfxDma_Dma_Channel g_chn;                               /* DMA channel handle                                       */

static IfxAsclin_Asc g_ascHandle;                       /* Declaration of the ASC handle                            */

/* Declaration of the FIFOs parameters */
static uint8 g_ascTxBuffer[UART_TX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];
static uint8 g_ascRxBuffer[UART_RX_BUFFER_SIZE + sizeof(Ifx_Fifo) + 8];

uint8 g_txData[] = {'H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd', '!'}; /* Message to send/receive via UART*/

Ifx_SizeT g_count = sizeof(g_txData);                   /* Size of the message                                      */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Interrupt triggered when the Tx PIN of the UART sends data */
IFX_INTERRUPT(asclin0TxISR, 0, INTPRIO_ASCLIN0_TX);

void asclin0TxISR(void)
{
    IfxAsclin_Asc_isrTransmit(&g_ascHandle);
}

/* Interrupt triggered when the DMA finishes a transaction */
IFX_INTERRUPT(prio_DMA, 0, INTPRIO_DMA);

void prio_DMA(void)
{
    g_correctTransactions++;
}

/* This function initializes the ASCLIN UART module */
void init_asclin_uart(void)
{
    /* Initialize an instance of IfxAsclin_Asc_Config with default values */
    IfxAsclin_Asc_Config ascConfig;
    IfxAsclin_Asc_initModuleConfig(&ascConfig, &MODULE_ASCLIN0);

    /* Set the desired baud rate */
    ascConfig.baudrate.baudrate = SERIAL_BAUDRATE;

    /* ISR priorities and interrupt service provider */
    ascConfig.interrupt.txPriority = INTPRIO_ASCLIN0_TX;
    ascConfig.interrupt.rxPriority = INTPRIO_ASCLIN0_RX;
    ascConfig.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* FIFO configuration */
    ascConfig.txBuffer = &g_ascTxBuffer;
    ascConfig.txBufferSize = UART_TX_BUFFER_SIZE;

    ascConfig.rxBuffer = &g_ascRxBuffer;
    ascConfig.rxBufferSize = UART_RX_BUFFER_SIZE;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins =
    {
        NULL_PTR,       IfxPort_InputMode_pullUp,       /* CTS port pin not used   */
        &UART_PIN_RX,   IfxPort_InputMode_pullUp,       /* RX port pin             */
        NULL_PTR,       IfxPort_OutputMode_pushPull,    /* RTS port pin not used   */
        &UART_PIN_TX,   IfxPort_OutputMode_pushPull,    /* TX port pin             */
        IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConfig.pins = &pins;

    IfxAsclin_Asc_initModule(&g_ascHandle, &ascConfig); /* Initialize module with above parameters                  */

    /* Modification of the TOS for the Rx related interruption */
    /* Change from CPU0 (previously defined above) to DMA */
    volatile Ifx_SRC_SRCR *src;
    src = IfxAsclin_getSrcPointerRx(ascConfig.asclin);

    /* Assign DMA as Service Provider when INTPRIO_ASCLIN0_RX is triggered */
    IfxSrc_init(src, IfxSrc_Tos_dma, INTPRIO_ASCLIN0_RX);

    IfxAsclin_enableRxFifoFillLevelFlag(ascConfig.asclin, TRUE);
    IfxSrc_enable(src);
}

/* Transmit data stored in the global variable g_txData[] on the UART via TX */
void send_data(void)
{
    IfxAsclin_Asc_write(&g_ascHandle, g_txData, &g_count, TIME_INFINITE); /* Transmit data via TX                   */
}

/* This function is called from main in order to initialize the DMA module */
void init_dma(void)
{
    /* Initialize an instance of IfxDma_Dma_Config with default values */
    IfxDma_Dma_Config dmaConfig;
    IfxDma_Dma_initModuleConfig(&dmaConfig, &MODULE_DMA);

    /* Initialize module */
    IfxDma_Dma dma;
    IfxDma_Dma_initModule(&dma, &dmaConfig);

    /* Initial configuration for all channels */
    IfxDma_Dma_ChannelConfig cfg;
    IfxDma_Dma_initChannelConfig(&cfg, &dma);

    /* Following configuration is used by the DMA channel */
    cfg.transferCount = N_TRANSFER;
    cfg.moveSize = IfxDma_ChannelMoveSize_8bit;
    cfg.blockMode = IfxDma_ChannelMove_1;

    /* DMA completes a full transaction on requests */
    cfg.requestMode = IfxDma_ChannelRequestMode_completeTransactionPerRequest;

    /* DMA as Interrupt Service Provider */
    cfg.hardwareRequestEnabled = TRUE;

    /* DMA channel stays enabled after one request */
    cfg.operationMode = IfxDma_ChannelOperationMode_continuous;


    /*************** Source and destination addresses ***************/

    /* Source address is not modified after a transfer */
    cfg.sourceAddressCircularRange = IfxDma_ChannelIncrementCircular_none;

    /* The element to transfer is always at the same location */
    cfg.sourceCircularBufferEnabled = TRUE;
    cfg.destinationAddressCircularRange = IfxDma_ChannelIncrementCircular_none;

    /* Copy the elements sequentially next to each others */
    cfg.destinationCircularBufferEnabled = FALSE;


    /*************** Channel specific configurations ***************/

    /* Select the Channel 12, related to the interruption on AscLin RX */
    cfg.channelId = (IfxDma_ChannelId) DMA_CHANNEL;

    /* Address of the UART RX FIFO */
    cfg.sourceAddress = (uint32) &g_ascHandle.asclin->RXDATA.U;

    /* Address of DLMU0 */
    cfg.destinationAddress = DEST_ADDR;
    cfg.channelInterruptEnabled = TRUE;

    /* DMA triggers an interrupt once the full transaction is done */
    cfg.channelInterruptControl = IfxDma_ChannelInterruptControl_thresholdLimitMatch;

    /* Priority of the channel interrupt trigger */
    cfg.channelInterruptPriority = INTPRIO_DMA;

    /* Interrupt service provider */
    cfg.channelInterruptTypeOfService = IfxSrc_Tos_cpu0;

    /* Initialize the DMA channel */
    IfxDma_Dma_initChannel(&g_chn, &cfg);
}
