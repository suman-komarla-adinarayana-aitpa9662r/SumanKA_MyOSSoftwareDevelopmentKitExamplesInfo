#ifndef IFX_ONEEYEDASPIPE_H
#define IFX_ONEEYEDASPIPE_H

#include "Platform_Types.h"
#include "IfxStdIf.h"
#include "IfxStdIf_DPipe.h"
#include "Ifx_OneEyeDasFifo.h"

typedef struct
{
        uint32 size;    // size in bytes, limited to maximum 1024 bytes
} Ifx_OneEyeDasPipe_Config;


typedef struct
{
        uint64 marker;
        struct
        {
                uint8 major;
                uint8 minor;
                uint8 patch;
                uint8 dummy;
        } Version;
} Ifx_OneEyeDasPipe_Marker;


typedef struct
{
        Ifx_OneEyeDasPipe_Marker oneEyeDebugPipe;
        Ifx_OneEyeDasFifo fifo;
        uint32 writeCount;
        uint32 readCount;
        Ifx_TickTime txTimestamp;
        boolean isError;
} Ifx_OneEyeDasPipe;

extern void Ifx_OneEyeDasPipe_initConfig(Ifx_OneEyeDasPipe_Config * config);
extern boolean Ifx_OneEyeDasPipe_init (Ifx_OneEyeDasPipe * dasPipe, Ifx_OneEyeDasPipe_Config * config);
extern boolean Ifx_OneEyeDasPipe_stdIfDPipeInit (IfxStdIf_DPipe *stdif, Ifx_OneEyeDasPipe *dpipe);

#endif
