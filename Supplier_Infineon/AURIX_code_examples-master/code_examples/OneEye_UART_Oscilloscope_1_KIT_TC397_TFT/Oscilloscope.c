/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "Oscilloscope.h"
#include "ifx_oe_osci.h"
#include "ifx_oe_oscibb.h"
#include "SignalGenerator.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define SAMPLE_PERIOD               1e-3                    /* Sampling period in seconds                           */
#define BB_LOCAL_PORT_OSCILLOSCOPE  (3)                     /* Local port for the BB protocol oscilloscope channel  */
#define BB_REMOTE_PORT_OSCILLOSCOPE (3)                     /* Remote port for the BB protocol oscilloscope channel */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
/* Oscilloscope object */
Ifx_Oe_Osci g_osci;

/* Synch protocol oscilloscope object */
Ifx_Oe_OsciBb g_osciBb;

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void initOscilloscope(Ifx_Oe_SyncProtocol* bb, float32 samplePeriod_ms)
{
    /* Initialize the oscilloscope */
    Ifx_Oe_Osci_Config osciCfg;
    Ifx_Oe_Osci_initConfig(&osciCfg);

    osciCfg.autoAddChannels = TRUE;                             /* When a signal is created, automatically assign
                                                                 * the signal to a channel if there is a free one   */
    osciCfg.samplePeriod    = samplePeriod_ms * 1e-3;               /* Sampling period in s                         */
    osciCfg.triggerMode     = Ifx_Oe_Osci_TriggerMode_automatic;    /* Oscilloscope mode after initialization
                                                                     * (can be changed at runtime from OneEye)      */

    /* Apply the configuration to the Oscilloscope */
    Ifx_Oe_Osci_init(&g_osci, &osciCfg);

    /* Add Signals to the oscilloscope */
    Ifx_Oe_Osci_addSignal(&g_osci, "Signal A (V)", Ifx_Oe_Osci_DataType_Float32, &g_signalGenerator.output.signalA, 0);
    Ifx_Oe_Osci_addSignal(&g_osci, "Signal B",     Ifx_Oe_Osci_DataType_SInt32,  &g_signalGenerator.output.signalB, 0);

    /* Initialize the BB protocol channel assigned to the oscilloscope */
    Ifx_Oe_OsciBb_init(&g_osciBb, &g_osci, bb, BB_LOCAL_PORT_OSCILLOSCOPE, BB_REMOTE_PORT_OSCILLOSCOPE);

    /* Start the oscilloscope */
    Ifx_Oe_Osci_start(&g_osci);
}


void processOscilloscope(void)
{
    /* Process the BB channel for the oscilloscope */
    Ifx_Oe_OsciBb_processMessages(&g_osciBb);
}


void sampleOscilloscope(void)
{
    /* Sample the signals */
    Ifx_Oe_Osci_step(&g_osci);
}
