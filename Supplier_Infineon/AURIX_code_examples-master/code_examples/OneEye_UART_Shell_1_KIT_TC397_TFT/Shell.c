/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "Shell.h"
#include "ifx_oe_shell.h"
#include "ifx_oe_shellbb.h"
#include "IfxGlobal_cfg.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/

/**  Local port for the BB protocol shell channel */
#define BB_LOCAL_PORT_SHELL (2)
/**  Remote port for the BB protocol shell channel */
#define BB_REMOTE_PORT_SHELL (2)

#    define KEY_CR "\r"
/** Define the shell Rx buffer size in byte. */
#define CFG_ASC0_RX_BUFFER_SIZE (128)
/** Define the shell Tx buffer size in byte. */
#define CFG_ASC0_TX_BUFFER_SIZE (1024)


/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
boolean printShellInfo(pchar args, void* data, IfxStdIf_DPipe* io);

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
/* Shell object */
Ifx_Oe_Shell g_shell;

/* Synch protocol shell object */
Ifx_Oe_ShellBb g_shellBb;

/* Array that stores the supported Shell commands */
const Ifx_Oe_Shell_Command g_shellCommands[] = {
    { "help", IFX_OE_SHELL_HELP_DESCRIPTION_TEXT, &g_shell, &Ifx_Oe_Shell_showHelp },
    { "info", "     : Dummy info command",        &g_shell, &printShellInfo        },
    IFX_OE_SHELL_COMMAND_LIST_END
};

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Function called when the shell command "info" is executed */
boolean printShellInfo(pchar args, void* data, IfxStdIf_DPipe* io)
{
    IfxStdIf_DPipe_print(io, "The shell command was called !" ENDL);
    return TRUE;
}


void initShell(Ifx_Oe_SyncProtocol* bb)
{
    /* Initialize the BB protocol channel assigned to the shell */
    Ifx_Oe_ShellBb_init(&g_shellBb, bb, BB_LOCAL_PORT_SHELL, BB_REMOTE_PORT_SHELL, CFG_ASC0_RX_BUFFER_SIZE, CFG_ASC0_TX_BUFFER_SIZE);

    /* Initialize the shell */
    Ifx_Oe_Shell_Config shellConf;
    Ifx_Oe_Shell_initConfig(&shellConf);                    /* Initialize the structure with default values         */

    shellConf.standardIo = &g_shellBb._dPipe.stdif;         /* Set a pointer to the standard interface              */

    shellConf.commandList[0] = &g_shellCommands[0];         /* Set the supported command list                       */

    /* Apply the configuration to the shell */
    Ifx_Oe_Shell_init(&g_shell, &shellConf);                /* Initialize the Shell with the given configuration    */

    /* Show some text in the console */
    IfxStdIf_DPipe_print(g_shell.io, "Hello world!"KEY_CR);
    IfxStdIf_DPipe_print(g_shell.io, KEY_CR);
}


void processShell(void)
{
    /* Process the BB channel for the shell */
    Ifx_Oe_ShellBb_processMessages(&g_shellBb);

    /* Process the shell commands */
    Ifx_Oe_Shell_process(&g_shell);
}
