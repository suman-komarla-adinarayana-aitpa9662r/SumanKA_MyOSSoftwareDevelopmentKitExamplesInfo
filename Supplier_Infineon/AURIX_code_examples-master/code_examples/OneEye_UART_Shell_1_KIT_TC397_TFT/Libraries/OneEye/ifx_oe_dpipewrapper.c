void IfxStdIf_DPipe_initStdIf(IfxStdIf_DPipe* stdIf)
{
    stdIf->driver     = NULL_PTR;
    stdIf->txDisabled = FALSE;

    /* *INDENT-OFF* */
    stdIf->write                   = (IfxStdIf_DPipe_Write         )&IfxStdIf_DPipe_nopWrite               ;
    stdIf->read                    = (IfxStdIf_DPipe_Read          )&IfxStdIf_DPipe_nopRead                ;
    stdIf->getReadCount            = (IfxStdIf_DPipe_GetReadCount  )&IfxStdIf_DPipe_nopGetReadCount        ;
    stdIf->getReadEvent            = (IfxStdIf_DPipe_GetReadEvent  )&IfxStdIf_DPipe_nopGetReadEvent        ;
    stdIf->getWriteCount           = (IfxStdIf_DPipe_GetWriteCount )&IfxStdIf_DPipe_nopGetWriteCount       ;
    stdIf->getWriteEvent           = (IfxStdIf_DPipe_GetWriteEvent )&IfxStdIf_DPipe_nopGetWriteEvent       ;
    stdIf->canReadCount            = (IfxStdIf_DPipe_CanReadCount  )&IfxStdIf_DPipe_nopCanReadCount        ;
    stdIf->canWriteCount           = (IfxStdIf_DPipe_CanWriteCount )&IfxStdIf_DPipe_nopCanWriteCount       ;
    stdIf->flushTx                 = (IfxStdIf_DPipe_FlushTx       )&IfxStdIf_DPipe_nopFlushTx             ;
    stdIf->clearTx                 = (IfxStdIf_DPipe_ClearTx       )&IfxStdIf_DPipe_nopClearTx             ;
    stdIf->clearRx                 = (IfxStdIf_DPipe_ClearRx       )&IfxStdIf_DPipe_nopClearRx             ;
    stdIf->onReceive               = (IfxStdIf_DPipe_OnReceive     )&IfxStdIf_DPipe_nopOnReceive           ;
    stdIf->onTransmit              = (IfxStdIf_DPipe_OnTransmit    )&IfxStdIf_DPipe_nopOnTransmit          ;
    stdIf->onError                 = (IfxStdIf_DPipe_OnError       )&IfxStdIf_DPipe_nopOnError             ;
    stdIf->getSendCount            = (IfxStdIf_DPipe_GetSendCount  )&IfxStdIf_DPipe_nopGetSendCount        ;
    stdIf->getTxTimeStamp          = (IfxStdIf_DPipe_GetTxTimeStamp)&IfxStdIf_DPipe_nopGetTxTimeStamp      ;
    stdIf->resetSendCount          = (IfxStdIf_DPipe_ResetSendCount)&IfxStdIf_DPipe_nopResetSendCount      ;
    /* *INDENT-ON* */
}
