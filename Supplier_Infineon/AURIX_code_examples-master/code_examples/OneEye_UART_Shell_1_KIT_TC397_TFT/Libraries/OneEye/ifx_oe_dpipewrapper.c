#include "ifx_oe_dpipewrapper.h"
#include <string.h>
#include <stdio.h>

void IfxStdIf_DPipe_printVA(IfxStdIf_DPipe* stdIf, Ifx_Oe_TickTime timeout, pchar format, va_list args)
{
    if (!stdIf->txDisabled)
    {
        char         message[STDIF_DPIPE_MAX_PRINT_SIZE + 1];
        Ifx_Oe_SizeT count;
        vsprintf((char*)message, format, args);
        count = (Ifx_Oe_SizeT)strlen(message);
        IFX_OE_ASSERT(count < STDIF_DPIPE_MAX_PRINT_SIZE);
        IfxStdIf_DPipe_write(stdIf, (void*)message, &count, timeout);
    }
}


void IfxStdIf_DPipe_writeText(IfxStdIf_DPipe* stdIf, Ifx_Oe_TickTime timeout, pchar text)
{
    if (!stdIf->txDisabled)
    {
        Ifx_Oe_SizeT count;
        count = (Ifx_Oe_SizeT)strlen(text);
        IfxStdIf_DPipe_write(stdIf, (void*)text, &count, timeout);
    }
}


